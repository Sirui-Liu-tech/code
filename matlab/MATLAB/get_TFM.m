function TF_matrix = get_TFM(mcg)
    wavename='sym6';
    totalscal=320; 
    %?????????scal???
    wcf=centfrq(wavename); 
    %???????
    cparam=2*wcf*totalscal; 
    %??????????????
    a=totalscal:-1:1;
    scal=cparam./a; 
    %??????????????????????
    TF_coefs=cwt(mcg,scal,wavename); 
    
    original_cols = size(TF_coefs, 2);
    target_cols = 320;
    x = linspace(1, original_cols, original_cols); % ????????
    y = 1:size(TF_coefs, 1); % ????????
    xq = linspace(1, original_cols, target_cols); % ????????
    yq = 1:size(TF_coefs, 1); % ????????
    
    [X, Y] = meshgrid(x, y);
    [Xq, Yq] = meshgrid(xq, yq);
    
    TF_matrix = interp2(X, Y, TF_coefs, Xq, Yq);
end


